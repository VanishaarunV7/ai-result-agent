const PDFDocument = require('pdfkit');
const fs = require('fs');
const mongoose = require('mongoose');
const Student = require('../models/student');
const StudentAnswer = require('../models/studentAnswer');
const Groq = require('groq-sdk');
const groq = new Groq({ apiKey: process.env.GROQ_API_KEY });

function getGrade(pct) {
  if (pct >= 90) return 'O';
  if (pct >= 80) return 'A+';
  if (pct >= 70) return 'A';
  if (pct >= 60) return 'B+';
  if (pct >= 50) return 'B';
  if (pct >= 40) return 'C';
  return 'F';
}

async function getRawAttempts(studentId) {
  const col = mongoose.connection.db.collection('studentexamattempts');
  return await col.find({ studentId, status: 'SUBMITTED' }).sort({ submittedAt: 1 }).toArray();
}

function drawHeader(doc, title) {
  doc.rect(0, 0, doc.page.width, 100).fill('#6C63FF');
  doc.fillColor('#FFFFFF').fontSize(24).font('Helvetica-Bold').text('EduAgent Academy', 50, 30);
  doc.fontSize(12).font('Helvetica').text('Excellence in Education', 50, 60);
  doc.fontSize(20).font('Helvetica-Bold').text(title, doc.page.width - 300, 40, { align: 'right', width: 250 });
  doc.fillColor('#333333');
}

function drawFooter(doc, pageNum) {
  const bottom = doc.page.height - 50;
  doc.moveTo(50, bottom).lineTo(doc.page.width - 50, bottom).lineWidth(1).stroke('#E5E7EB');
  doc.fontSize(10).fillColor('#9CA3AF').text(`Generated by EduAgent AI • Page ${pageNum}`, 50, bottom + 15, { align: 'center', width: doc.page.width - 100 });
}

function drawCard(doc, x, y, width, height, title, value, subtext) {
  doc.roundedRect(x, y, width, height, 8).fillAndStroke('#F9FAFB', '#E5E7EB');
  doc.fillColor('#6B7280').fontSize(10).font('Helvetica-Bold').text(title.toUpperCase(), x + 15, y + 15);
  doc.fillColor('#111827').fontSize(22).font('Helvetica-Bold').text(value, x + 15, y + 35);
  if (subtext) {
    doc.fillColor('#6C63FF').fontSize(10).font('Helvetica').text(subtext, x + 15, y + 65);
  }
}

exports.downloadReport = async (req, res) => {
  try {
    const { studentId } = req.query;
    if (!studentId) return res.status(400).json({ error: 'studentId required' });

    const student = await Student.findOne({ $or: [{ _id: studentId }, { academicNo: studentId }] });
    const studentName = student ? student.name : 'Unknown Student';
    const program = student ? student.program_id : 'General';

    const attempts = await getRawAttempts(studentId);
    if (!attempts.length) {
      return res.status(404).json({ error: 'No exam attempts found for this student.' });
    }

    const answers = await StudentAnswer.find({ studentId });

    // Performance Calculations
    const sortedExams = [...attempts].sort((a,b) => new Date(a.submittedAt) - new Date(b.submittedAt));
    const totalExams = sortedExams.length;
    const totalScored = sortedExams.reduce((s, e) => s + e.marksScored, 0);
    const totalMax = sortedExams.reduce((s, e) => s + e.totalMarks, 0);
    const overallPct = Math.round((totalScored / totalMax) * 100) || 0;
    const overallGrade = getGrade(overallPct);

    let improvementStr = 'N/A';
    if (totalExams >= 2) {
      const first = sortedExams[0].percentage;
      const latest = sortedExams[totalExams - 1].percentage;
      const diff = latest - first;
      improvementStr = `${diff > 0 ? '+' : ''}${diff.toFixed(1)}%`;
    }

    // Topic logic
    const topicMap = {};
    answers.forEach(ans => {
      const t = ans.topic?.name;
      if (!t) return;
      if (!topicMap[t]) topicMap[t] = { scored: 0, max: 0 };
      topicMap[t].scored += ans.marksScored;
      topicMap[t].max += ans.maxMarks;
    });

    const topicResults = Object.entries(topicMap).map(([topic, data]) => {
      const pct = Math.round((data.scored / data.max) * 100);
      return { topic, pct };
    }).sort((a, b) => b.pct - a.pct);

    const strongTopics = topicResults.slice(0, 3).filter(t => t.pct >= 60);
    const weakTopics = [...topicResults].sort((a, b) => a.pct - b.pct).slice(0, 3).filter(t => t.pct < 60);

    // AI Feedback
    let aiFeedback = "Keep up the good work!";
    try {
      const prompt = `Generate a 2-3 sentence academic report card feedback for ${studentName}. 
      Overall: ${overallPct}%. Exams attended: ${totalExams}. 
      Strong topics: ${strongTopics.map(t=>t.topic).join(', ') || 'None'}. 
      Weak topics: ${weakTopics.map(t=>t.topic).join(', ') || 'None'}. 
      Be encouraging and professional like a teacher.`;

      const completion = await groq.chat.completions.create({
        messages: [{ role: 'user', content: prompt }],
        model: 'llama-3.3-70b-versatile',
        temperature: 0.3,
        max_tokens: 100
      });
      aiFeedback = completion.choices[0]?.message?.content || aiFeedback;
    } catch (e) {
      console.log('AI feedback failed, using fallback.');
    }

    // PDF Generation
    const doc = new PDFDocument({ margin: 50, size: 'A4' });
    const filename = `Academic_Report_${studentId}.pdf`;
    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="${filename}"`);
    doc.pipe(res);

    // --- PAGE 1 ---
    drawHeader(doc, 'ACADEMIC PROGRESS REPORT');

    // Student Info
    doc.roundedRect(50, 120, doc.page.width - 100, 90, 8).fillAndStroke('#FFFFFF', '#E5E7EB');
    doc.fillColor('#111827').fontSize(16).font('Helvetica-Bold').text(studentName, 70, 140);
    doc.fillColor('#6B7280').fontSize(11).font('Helvetica')
       .text(`Student ID: ${studentId}`, 70, 165)
       .text(`Program: ${program}`, 70, 180)
       .text(`Report Date: ${new Date().toLocaleDateString()}`, doc.page.width / 2, 165)
       .text(`Batch: 2026`, doc.page.width / 2, 180);

    // Summary Cards
    const cardY = 230;
    const cardW = 150;
    const cardH = 90;
    const gap = (doc.page.width - 100 - (cardW * 3)) / 2;

    drawCard(doc, 50, cardY, cardW, cardH, 'Overall %', `${overallPct}%`, `Grade: ${overallGrade}`);
    drawCard(doc, 50 + cardW + gap, cardY, cardW, cardH, 'Exams Attended', `${totalExams}`, 'Total Attempts');
    drawCard(doc, 50 + (cardW + gap) * 2, cardY, cardW, cardH, 'Improvement', improvementStr, 'vs First Exam');

    // Rank & Attendance (Mocked)
    const attY = 340;
    doc.roundedRect(50, attY, (doc.page.width - 120) / 2, 120, 8).fillAndStroke('#FFFFFF', '#E5E7EB');
    doc.fillColor('#111827').fontSize(14).font('Helvetica-Bold').text('Attendance Record', 70, attY + 20);
    doc.fillColor('#4B5563').fontSize(11).font('Helvetica')
       .text('Working Days: 220', 70, attY + 50)
       .text('Present Days: 205', 70, attY + 70)
       .text('Absent Days: 15', 70, attY + 90);
    doc.fillColor('#10B981').fontSize(24).font('Helvetica-Bold').text('93%', 50 + (doc.page.width - 120) / 4 + 20, attY + 60);

    doc.roundedRect(50 + (doc.page.width - 120) / 2 + 20, attY, (doc.page.width - 120) / 2, 120, 8).fillAndStroke('#FFFFFF', '#E5E7EB');
    doc.fillColor('#111827').fontSize(14).font('Helvetica-Bold').text('Grading Scale', 50 + (doc.page.width - 120) / 2 + 40, attY + 20);
    doc.fillColor('#4B5563').fontSize(10).font('Helvetica')
       .text('O  : ≥ 90%', 50 + (doc.page.width - 120) / 2 + 40, attY + 50)
       .text('A+ : 80 - 89%', 50 + (doc.page.width - 120) / 2 + 40, attY + 70)
       .text('A  : 70 - 79%', 50 + (doc.page.width - 120) / 2 + 40, attY + 90)
       .text('B+ : 60 - 69%', 50 + (doc.page.width - 120) / 2 + 140, attY + 50)
       .text('B  : 50 - 59%', 50 + (doc.page.width - 120) / 2 + 140, attY + 70)
       .text('F  : < 40%', 50 + (doc.page.width - 120) / 2 + 140, attY + 90);

    // AI Feedback
    const aiY = 480;
    doc.roundedRect(50, aiY, doc.page.width - 100, 100, 8).fillAndStroke('#F3F4F6', '#E5E7EB');
    doc.fillColor('#6C63FF').fontSize(12).font('Helvetica-Bold').text('AI Teacher Feedback', 70, aiY + 20);
    doc.fillColor('#4B5563').fontSize(11).font('Helvetica-Oblique').text(`"${aiFeedback}"`, 70, aiY + 45, { width: doc.page.width - 140, lineGap: 4 });

    drawFooter(doc, 1);

    // --- PAGE 2 ---
    doc.addPage();
    drawHeader(doc, 'DEEP DIVE ANALYTICS');

    // Exam Comparison Table
    let currentY = 120;
    doc.fillColor('#111827').fontSize(14).font('Helvetica-Bold').text('Exam History', 50, currentY);
    currentY += 20;

    // Table Header
    doc.rect(50, currentY, doc.page.width - 100, 25).fill('#F9FAFB');
    doc.fillColor('#6B7280').fontSize(10).font('Helvetica-Bold');
    doc.text('Date', 60, currentY + 8);
    doc.text('Exam Name', 160, currentY + 8);
    doc.text('Course', 340, currentY + 8);
    doc.text('Percentage', 440, currentY + 8);
    doc.text('Grade', 510, currentY + 8);
    currentY += 25;

    // Table Rows
    doc.font('Helvetica').fontSize(10).fillColor('#333333');
    sortedExams.forEach((ex, idx) => {
      const dt = new Date(ex.submittedAt).toLocaleDateString();
      const h = 25;
      if (idx % 2 === 0) doc.rect(50, currentY, doc.page.width - 100, h).fill('#FFFFFF');
      else doc.rect(50, currentY, doc.page.width - 100, h).fill('#F9FAFB');
      
      doc.fillColor('#333333');
      doc.text(dt, 60, currentY + 8);
      doc.text(ex.examName, 160, currentY + 8, { width: 170, height: 12, ellipsis: true });
      doc.text(ex.courseName || 'Unknown', 340, currentY + 8, { width: 90, height: 12, ellipsis: true });
      doc.fillColor(ex.percentage >= 60 ? '#10B981' : '#EF4444').text(`${ex.percentage}%`, 440, currentY + 8);
      doc.fillColor('#333333').text(getGrade(ex.percentage), 510, currentY + 8);
      currentY += h;
    });

    doc.moveTo(50, currentY).lineTo(doc.page.width - 50, currentY).stroke('#E5E7EB');

    // Trend Chart (Bar Chart)
    currentY += 40;
    doc.fillColor('#111827').fontSize(14).font('Helvetica-Bold').text('Performance Trend', 50, currentY);
    currentY += 30;

    const chartX = 50;
    const chartY = currentY;
    const chartW = doc.page.width - 100;
    const chartH = 150;
    
    // Y-Axis labels and lines
    doc.font('Helvetica').fontSize(8).fillColor('#9CA3AF');
    [0, 25, 50, 75, 100].forEach(val => {
      const yLine = chartY + chartH - (val / 100) * chartH;
      doc.text(`${val}%`, chartX, yLine - 4);
      doc.moveTo(chartX + 25, yLine).lineTo(chartX + chartW, yLine).lineWidth(0.5).stroke('#E5E7EB');
    });

    // Bars
    if (totalExams > 0) {
      const barSpacing = (chartW - 40) / totalExams;
      const barW = Math.min(barSpacing * 0.6, 40);
      
      sortedExams.forEach((ex, idx) => {
        const xPos = chartX + 40 + (idx * barSpacing) + (barSpacing - barW) / 2;
        const barH = (ex.percentage / 100) * chartH;
        const yPos = chartY + chartH - barH;
        
        doc.rect(xPos, yPos, barW, barH).fill('#6C63FF');
        
        doc.fillColor('#333333').fontSize(8).text(ex.examName.substring(0, 8) + '...', xPos - 5, chartY + chartH + 10, { width: barW + 10, align: 'center' });
        doc.fillColor('#6C63FF').fontSize(9).font('Helvetica-Bold').text(`${ex.percentage}%`, xPos - 5, yPos - 12, { width: barW + 10, align: 'center' });
      });
    }

    currentY += chartH + 50;

    // Strong/Weak Topics
    doc.roundedRect(50, currentY, (doc.page.width - 120) / 2, 100, 8).fillAndStroke('#FFFFFF', '#E5E7EB');
    doc.fillColor('#10B981').fontSize(12).font('Helvetica-Bold').text('🌟 Strong Topics', 70, currentY + 15);
    doc.fillColor('#4B5563').fontSize(10).font('Helvetica');
    if (strongTopics.length) {
      strongTopics.forEach((t, i) => doc.text(`• ${t.topic} (${t.pct}%)`, 70, currentY + 40 + (i * 15)));
    } else {
      doc.text('No strong topics identified yet.', 70, currentY + 40);
    }

    doc.roundedRect(50 + (doc.page.width - 120) / 2 + 20, currentY, (doc.page.width - 120) / 2, 100, 8).fillAndStroke('#FFFFFF', '#E5E7EB');
    doc.fillColor('#EF4444').fontSize(12).font('Helvetica-Bold').text('⚠️ Areas for Improvement', 50 + (doc.page.width - 120) / 2 + 40, currentY + 15);
    doc.fillColor('#4B5563').fontSize(10).font('Helvetica');
    if (weakTopics.length) {
      weakTopics.forEach((t, i) => doc.text(`• ${t.topic} (${t.pct}%)`, 50 + (doc.page.width - 120) / 2 + 40, currentY + 40 + (i * 15)));
    } else {
      doc.text('No weak topics identified yet.', 50 + (doc.page.width - 120) / 2 + 40, currentY + 40);
    }

    drawFooter(doc, 2);

    doc.end();
    
  } catch (error) {
    console.error('PDF Generation Error:', error);
    if (!res.headersSent) {
      res.status(500).json({ error: error.message });
    }
  }
};
